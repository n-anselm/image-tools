def interactive_configure(args: argparse.Namespace) -> argparse.Namespace:
    """
    Guide the user through configuration in the terminal.

    Overrides any command-line settings with the user's interactive choices.
    """
    print("\n" + "=" * 60)
    print("  Interactive Configuration Mode")
    print("=" * 60 + "\n")

    # --- Resolution ---
    resolutions = {
        "1": ("full", "original image resolution (largest file)"),
        "2": ("half", "half resolution (good balance, default)"),
        "3": ("quarter", "quarter resolution (smallest file)"),
    }
    print("Point cloud resolution:")
    for key, (name, desc) in resolutions.items():
        print(f"  {key}. {name} — {desc}")
    while True:
        choice = input("Enter choice [1-3, default: 2]: ").strip()
        if not choice:
            break
        if choice in resolutions:
            args.resolution = resolutions[choice][0]
            break
        print("  Invalid choice. Please enter 1, 2, or 3.")

    # --- Additional settings? ---
    if _ask_user("Do you want to specify additional settings?", default=False):
        # Depth map
        depth_input = input(
            "Depth map path (leave empty for auto-lookup): "
        ).strip()
        if depth_input:
            args.depth_input = depth_input

        # EXIF
        exif_input = input(
            "EXIF JSON path (leave empty for auto-lookup): "
        ).strip()
        if exif_input:
            args.exif_input = exif_input

        # Depth inversion
        args.no_invert_depth = not _ask_user(
            "Re-invert depth so near objects have smaller Z values?",
            default=True,
        )

    print("\n" + "=" * 60)
    print("  Configuration complete!")
    print("=" * 60 + "\n")
    return args


