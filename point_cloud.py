"""
Created by OpenCode.
Initialized: 2026-05-20
Updated: 2026-05-20

Point Cloud Generator CLI.

Converts an RGB image and its depth map into a colored 3D point cloud
(saved as an ASCII .ply file). Uses the pinhole camera model to back-project
every pixel into 3D space.

By default, the script re-inverts the depth map (since depth_map.py inverts
for ControlNet compatibility: black = near, white = far). After re-inversion,
near objects map to smaller Z values and far objects to larger Z values,
which is the standard expectation in 3D viewers. Use --no-invert-depth to
skip re-inversion and treat the depth map as-is.

Camera intrinsics are loaded automatically from an EXIF JSON file
(<stem>-exif.json, generated by exif_extractor.py). If unavailable, generic
fallback intrinsics are used (50 mm equivalent focal length on a 36 mm sensor).

Usage:
    python point_cloud.py photo.png [output.ply] [options]

Examples:
    # Auto-look for depth map and intrinsics, output photo-pointcloud.ply
    python point_cloud.py photo.png

    # Explicit depth map and intrinsics
    python point_cloud.py photo.png --depth photo-depthmap.png --exif photo-exif.json

    # Full resolution (larger file, more detail)
    python point_cloud.py photo.png --resolution full

    # Interactive configuration
    python point_cloud.py photo.png -i
"""

