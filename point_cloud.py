def load_intrinsics(
    exif_path: Path | None, width: int, height: int
) -> tuple[float, float, float, float]:
    """
    Load or compute camera intrinsics (fx, fy, cx, cy).

    Args:
        exif_path: Path to EXIF JSON, or None for fallback.
        width: Image width in pixels.
        height: Image height in pixels.

    Returns:
        (fx, fy, cx, cy) as floats.
    """
    cx = width / 2.0
    cy = height / 2.0

    if exif_path is not None and exif_path.exists():
        try:
            with open(exif_path, "r", encoding="utf-8") as f:
                data = json.load(f)

            intrinsics = data.get("computed_intrinsics", {})
            focal_px = intrinsics.get("focal_length_px")

            if focal_px is not None:
                fx = float(focal_px)
                fy = fx  # assume square pixels
                logger.info(
                    "Loaded intrinsics from EXIF: fx=%.2f, fy=%.2f, cx=%.2f, cy=%.2f",
                    fx, fy, cx, cy,
                )
                return fx, fy, cx, cy

            # Try deriving from focal_length_mm + sensor width
            focal_mm = data.get("focal_length_mm")
            focal_35 = data.get("focal_length_35mm_equiv")

            if focal_mm is not None:
                # If sensor info not in computed_intrinsics, estimate from 35mm equiv
                if focal_35 is not None:
                    # sensor_width = focal_mm * 36 / focal_35
                    sensor_width = focal_mm * 36.0 / focal_35
                else:
                    sensor_width = 36.0  # fallback full-frame assumption

                fx = float(focal_mm) * width / sensor_width
                fy = fx  # square pixels
                logger.info(
                    "Derived intrinsics from focal length: fx=%.2f, fy=%.2f, "
                    "cx=%.2f, cy=%.2f (sensor_width=%.2f mm)",
                    fx, fy, cx, cy, sensor_width,
                )
                return fx, fy, cx, cy

        except Exception as exc:
            logger.warning("Failed to read EXIF JSON (%s). Using fallback intrinsics.", exc)

    # Generic fallback
    focal_mm = 50.0
    sensor_width_mm = 36.0
    fx = focal_mm * width / sensor_width_mm
    fy = fx
    logger.warning(
        "Using fallback intrinsics: fx=%.2f, fy=%.2f, cx=%.2f, cy=%.2f "
        "(50 mm equiv on 36 mm sensor). Point cloud scale will be approximate.",
        fx, fy, cx, cy,
    )
    return fx, fy, cx, cy


def load_image_bgr(path: Path) -> np.ndarray | None:
    """Load an image with OpenCV, preserving EXIF orientation."""
    from PIL import Image as PILImage
    from PIL import ImageOps

    try:
        pil_img = PILImage.open(str(path))
        pil_img = ImageOps.exif_transpose(pil_img)
        img_rgb = np.array(pil_img)

        if len(img_rgb.shape) == 3 and img_rgb.shape[2] >= 3:
            if img_rgb.shape[2] == 3:
                return cv2.cvtColor(img_rgb, cv2.COLOR_RGB2BGR)
            return cv2.cvtColor(img_rgb, cv2.COLOR_RGBA2BGRA)
        return img_rgb
    except Exception as exc:
        logger.error("Failed to load image: %s — %s", path, exc)
        return None


def create_point_cloud(
    rgb: np.ndarray,
    depth: np.ndarray,
    fx: float,
    fy: float,
    cx: float,
    cy: float,
    invert_depth: bool,
) -> tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray, np.ndarray, np.ndarray]:
    """
    Back-project an RGB image and depth map into 3D points.

    Args:
        rgb: BGR image, shape (H, W, 3).
        depth: Grayscale depth map, shape (H, W) or (H, W, 1).
        fx, fy: Focal lengths in pixels.
        cx, cy: Principal point.
        invert_depth: If True, re-invert depth so near -> small Z.

    Returns:
        Tuple of flat arrays (X, Y, Z, R, G, B).
    """
    h, w = rgb.shape[:2]

    # Ensure depth is single channel
    if len(depth.shape) == 3:
        depth_gray = cv2.cvtColor(depth, cv2.COLOR_BGR2GRAY)
    else:
        depth_gray = depth

    # Normalize depth to [0, 1]
    depth_norm = depth_gray.astype(np.float32) / 255.0

    # Re-invert depth by default (depth_map.py inverts for ControlNet:
    # black = near, white = far). Re-inverting restores:
    #   small Z value = near, large Z value = far.
    if invert_depth:
        depth_norm = 1.0 - depth_norm

    # Coordinate grids
    u = np.arange(w, dtype=np.float32)
    v = np.arange(h, dtype=np.float32)
    uu, vv = np.meshgrid(u, v)

    # Back-projection
    Z = depth_norm  # relative depth; scale is arbitrary for monocular depth
    X = (uu - cx) * Z / fx
    Y = (vv - cy) * Z / fy

    # Extract colors (BGR → RGB for PLY)
    b = rgb[:, :, 0].astype(np.uint8)
    g = rgb[:, :, 1].astype(np.uint8)
    r = rgb[:, :, 2].astype(np.uint8)

    return (
        X.ravel(),
        Y.ravel(),
        Z.ravel(),
        r.ravel(),
        g.ravel(),
        b.ravel(),
    )


def write_ply(
    path: Path,
    X: np.ndarray,
    Y: np.ndarray,
    Z: np.ndarray,
    R: np.ndarray,
    G: np.ndarray,
    B: np.ndarray,
) -> bool:
    """
    Write a point cloud to an ASCII .ply file.

    Args:
        path: Output file path.
        X, Y, Z: Float coordinate arrays.
        R, G, B: UInt8 color arrays.

    Returns:
        True on success.
    """
    n_points = X.shape[0]
    header = (
        "ply\n"
        "format ascii 1.0\n"
        f"element vertex {n_points}\n"
        "property float x\n"
        "property float y\n"
        "property float z\n"
        "property uchar red\n"
        "property uchar green\n"
        "property uchar blue\n"
        "end_header\n"
    )

    try:
        path.parent.mkdir(parents=True, exist_ok=True)
        points = np.column_stack([X, Y, Z, R, G, B])
        with open(path, "w", encoding="ascii") as f:
            f.write(header)
            np.savetxt(f, points, fmt="%.6f %.6f %.6f %d %d %d")
        logger.info("Saved point cloud: %s (%d points)", path, n_points)
        return True
    except Exception as exc:
        logger.error("Failed to write .ply file: %s", exc)
        return False


def resolve_output_path(rgb_path: Path, output_arg: str | None) -> Path:
    """Determine the output .ply file path."""
    if output_arg:
        out = Path(output_arg)
        if output_arg.endswith(("/", "\\")) or (out.exists() and out.is_dir()):
            return out / f"{rgb_path.stem}-pointcloud.ply"
        if out.suffix.lower() != ".ply":
            return out.with_suffix(".ply")
        return out
    return rgb_path.parent / f"{rgb_path.stem}-pointcloud.ply"


