def main() -> int:
    """Parse arguments, optionally enter interactive mode, and generate point cloud."""
    parser = _build_parser()
    args = parser.parse_args()

    # Interactive configuration overrides CLI flags
    if args.interactive:
        args = interactive_configure(args)

    # Resolve and validate input
    rgb_path = Path(args.input).resolve()
    if not rgb_path.exists():
        logger.error("Input image not found: %s", rgb_path)
        return 1

    if not rgb_path.is_file():
        logger.error("Input must be a single image file: %s", rgb_path)
        return 1

    if rgb_path.suffix.lower() not in SUPPORTED_EXTENSIONS:
        logger.error("Unsupported input file format: %s", rgb_path.suffix)
        return 1

    # Find depth map
    depth_path = find_depth_map(rgb_path, args.depth_input)
    if depth_path is None:
        return 1

    # Find EXIF JSON (optional — falls back to generic intrinsics)
    exif_path = find_exif_json(rgb_path, args.exif_input)

    # Resolve output path
    output_path = resolve_output_path(rgb_path, args.output)

    # Load images
    rgb = load_image_bgr(rgb_path)
    depth = load_image_bgr(depth_path)
    if rgb is None or depth is None:
        return 1

    # Determine target resolution
    h_orig, w_orig = rgb.shape[:2]
    scale_map = {"full": 1.0, "half": 0.5, "quarter": 0.25}
    scale = scale_map[args.resolution]
    target_w = int(w_orig * scale)
    target_h = int(h_orig * scale)

    if scale < 1.0:
        logger.info(
            "Resizing to %s resolution: %dx%d → %dx%d",
            args.resolution, w_orig, h_orig, target_w, target_h,
        )
        rgb = cv2.resize(rgb, (target_w, target_h), interpolation=cv2.INTER_LINEAR)
        depth = cv2.resize(depth, (target_w, target_h), interpolation=cv2.INTER_LINEAR)
    else:
        logger.info("Using full resolution: %dx%d", w_orig, h_orig)

    # Load intrinsics
    fx, fy, cx, cy = load_intrinsics(exif_path, target_w, target_h)

    # Summary before processing
    logger.info("Input:    %s", rgb_path)
    logger.info("Depth:    %s", depth_path)
    logger.info("EXIF:     %s", exif_path if exif_path else "(fallback)")
    logger.info("Output:   %s", output_path)
    logger.info("Size:     %dx%d", target_w, target_h)
    logger.info("Invert:   %s", not args.no_invert_depth)
    logger.info("Intrinsics: fx=%.2f, fy=%.2f, cx=%.2f, cy=%.2f", fx, fy, cx, cy)

    # Generate point cloud
    try:
        X, Y, Z, R, G, B = create_point_cloud(
            rgb, depth, fx, fy, cx, cy, invert_depth=not args.no_invert_depth,
        )
    except Exception as exc:
        logger.error("Failed to create point cloud: %s", exc)
        return 1

    # Save
    if not write_ply(output_path, X, Y, Z, R, G, B):
        return 1

    return 0


if __name__ == "__main__":
    sys.exit(main())
